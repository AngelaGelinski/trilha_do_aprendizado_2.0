/* Crie um script em que o usuário entre com um número o sistema exibia qual o dia da semana. Ex: 1"domingo"*/ 

var dia
dia=prompt(" Entre com o numero referente ao dua da semana")

switch(dia){
    case"1":{
        alert("Domingo")
        break
    }
    case"2":{
        alert("Segunda-feira")
        break
    }
    case"3":{
        alert("Terça-feira")
        break
    }
    case"4":{
        alert("Quarta-feira")
        break
    }
    case"5":{
        alert("Quinta-feira")
        break
    }
    case"6":{
        alert("Sexta-feira")
        break
    }
    case"7":{
        alert("Sabado")
        break
    }
}








